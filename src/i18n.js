let currentLanguage = loadLanguage();

function loadLanguage() {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (LANGUAGES.some((language) => language.code === saved)) return saved;
  } catch {
    // Local storage can be unavailable.
  }
  return 'en';
}

function saveLanguage() {
  try {
    localStorage.setItem(STORAGE_KEY, currentLanguage);
  } catch {
    // Local storage can be unavailable.
  }
}

export function getLanguage() {
  return currentLanguage;
}

export function setLanguage(code) {
  if (!LANGUAGES.some((language) => language.code === code)) return;
  currentLanguage = code;
  if (typeof document !== 'undefined') document.documentElement.lang = code;
  saveLanguage();
}

export function cycleLanguage() {
  const index = LANGUAGES.findIndex((language) => language.code === currentLanguage);
  const next = LANGUAGES[(index + 1) % LANGUAGES.length];
  setLanguage(next.code);
  return next.code;
}

export function t(key, params = {}) {
  const value = TEXT[currentLanguage]?.[key] ?? TEXT.en[key] ?? key;
  return value.replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? ''));
}

function localized(group, id) {
  return NAMES[group]?.[id]?.[currentLanguage] ?? NAMES[group]?.[id]?.en ?? id;
}

export function languageName(code = currentLanguage) {
  return TEXT[code]?.languageName ?? code;
}

