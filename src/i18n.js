export function abilityName(id) {
  return localized('ability', id);
}

export function consumableName(id) {
  return localized('consumable', id);
}

export function itemName(item) {
  if (!item) return '';
  if (item.type === 'ability') return abilityName(item.abilityId);
  if (item.type === 'consumable') return consumableName(item.consumableId);
  if (item.type === 'weapon_upgrade') {
    const labels = { en: 'Weapon', it: 'Arma', fr: 'Arme', de: 'Waffe' };
    return `${labels[currentLanguage] ?? labels.en} L${item.targetLevel}`;
  }
  if (item.type === 'super_upgrade') {
    const labels = { en: 'Super Weapon', it: 'Super arma', fr: 'Super arme', de: 'Superwaffe' };
    return `${labels[currentLanguage] ?? labels.en} L${item.targetLevel}`;
  }
  return localized('item', item.id);
}

export function itemDescription(item) {
  if (!item) return '';
  if (item.type === 'ability') {
    const desc = DESCRIPTIONS.ability[item.abilityId];
    return desc?.[currentLanguage] ?? desc?.en ?? '';
  }
  if (item.type === 'consumable') {
    const desc = DESCRIPTIONS.consumable[item.consumableId];
    return desc?.[currentLanguage] ?? desc?.en ?? '';
  }
  if (item.type === 'life_slot') return DESCRIPTIONS.life_slot[currentLanguage] ?? DESCRIPTIONS.life_slot.en;
  if (item.type === 'food') return DESCRIPTIONS.food[currentLanguage] ?? DESCRIPTIONS.food.en;
  if (item.type === 'weapon_upgrade') {
    const description = DESCRIPTIONS.weapon[item.targetLevel];
    return description?.[currentLanguage] ?? description?.en ?? `Weapon L${item.targetLevel}`;
  }
  if (item.type === 'super_upgrade') {
    const description = DESCRIPTIONS.super[item.targetLevel];
    return description?.[currentLanguage] ?? description?.en ?? `Super Weapon L${item.targetLevel}`;
  }
  return item.description ?? '';
}

export function actionName(id) {
  return localized('action', id);
}

